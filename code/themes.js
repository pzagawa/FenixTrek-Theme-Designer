class Themes
{
    static addThemeItem(theme_name, theme_code)
    {
        //row
        let el_tr = document.createElement("tr");

        //td image
        let el_td_image = document.createElement("td");
        el_td_image.className = "image";

        //td link
        let el_td_link = document.createElement("td");
        el_td_link.className = "link";

        //image
        let file_name = theme_name.toLowerCase();
        let el_img = document.createElement("img");
        el_img.src = `themes_list/${file_name}.png`;

        el_td_image.appendChild(el_img);

        //link
        let el_a = document.createElement("a");
        el_a.target = "_self";
        el_a.href = `designer.html?theme=${theme_code}`;
        el_a.innerText = theme_name;

        el_td_link.appendChild(el_a);

        el_tr.appendChild(el_td_image);
        el_tr.appendChild(el_td_link);

        //get list
        let el_themes_list = document.querySelector("#ThemesList");

        //create spacer
        let el_tr_spacer = document.createElement("tr");
        el_tr_spacer.className = "spacer";
        el_tr_spacer.innerHTML = "<td></td>";

        //add rows
        el_themes_list.appendChild(el_tr);
        el_themes_list.appendChild(el_tr_spacer);
    }
}

function onInitialized()
{
    var count = 0;

    for (let theme_name in ThemesData)
    {
        let theme_code = ThemesData[theme_name];
        Themes.addThemeItem(theme_name, theme_code);

        count++;
    }

    let el_themes_title = document.querySelector("#ThemesTitle");
    el_themes_title.innerText = "THEMES: " + count;
}

onInitialized();
